class LanguageData {
  final String name;
  final String code;
  final String flag;
  final List<WordData> words;

  const LanguageData({
    required this.name,
    required this.code,
    required this.flag,
    required this.words,
  });
}

class ExampleData {
  final String sentence;
  final String translation;

  const ExampleData({
    required this.sentence,
    required this.translation,
  });
}

